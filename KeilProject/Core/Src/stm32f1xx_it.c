/**
  * @brief This function handles USART1 global interrupt.
  */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */
	if(__HAL_UART_GET_FLAG(&huart1, UART_FLAG_IDLE) != RESET)
	{
		uint16_t l_val;
		l_val = UART_REVBUFFSIZE - DMA1_Channel5->CNDTR; // 通过DMA接收指针计算接收的字节数
//		printf("DMA l_val:%d\r\n",l_val);
		if(l_val > uart1str.RecvQue_Tail)
		{
			uart1str.Uart_RecvLens += l_val - uart1str.RecvQue_Tail;
		}
		else
		{
			uart1str.Uart_RecvLens += UART_REVBUFFSIZE - uart1str.RecvQue_Tail + l_val;
		}
		uart1str.RecvQue_Tail = l_val;
		uart1str.Uart_RecvLens %= UART_REVBUFFSIZE;
		__HAL_UART_CLEAR_IDLEFLAG(&huart1);
		osSemaphoreRelease(serialSemaphore1Handle);
	}
  /* USER CODE END USART1_IRQn 0 */
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */
	
  /* USER CODE END USART1_IRQn 1 */
}

